export const getLocalizedMessage = key =>
  Object.fromEntries(
    Object.entries(messages).map(([language, values]) => [language, values[key]])
  )

const interpolate = (text, values = {}) => text.replace(/\{(\w+)\}/g, (_, key) => String(values[key] ?? `{${key}}`))

export const createTranslator = i18nApi => (key, values) =>
  interpolate(i18nApi.resolveText(getLocalizedMessage(key), messages.en[key] ?? key), values)
