import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

import subsetFont from 'subset-font'

const scriptDir = path.dirname(fileURLToPath(import.meta.url))
const clientRoot = path.resolve(scriptDir, '..')
const repoRoot = path.resolve(clientRoot, '../..')

const sourceFontPath = path.join(
  clientRoot,
  'node_modules/material-symbols/material-symbols-rounded.woff2'
)
const symbolTypesPath = path.join(clientRoot, 'node_modules/material-symbols/index.d.ts')
const outputFontPath = path.join(clientRoot, 'src/assets/fonts/material-symbols-rounded.woff2')
const outputListPath = path.join(clientRoot, 'src/assets/fonts/material-symbols-rounded.icons.txt')

const sourceExtensions = new Set(['.css', '.js', '.jsx', '.json', '.scss', '.ts', '.tsx'])
const ignoredDirectories = new Set([
  '.git',
  '.turbo',
  'dist',
  'node_modules',
  'out'
])

const scanRoots = [
  path.join(clientRoot, 'src'),
  path.join(repoRoot, 'packages'),
  path.join(repoRoot, 'apps/desktop/src')
]

const explicitSymbols = [
  'expand_more',
  'star_border'
]

const isMaterialSymbol = value => /^[a-z0-9][a-z0-9_]*$/.test(value)

const formatBytes = bytes => {
  if (bytes < 1024) return `${bytes} B`
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`
}

const pathExists = async value => {
  try {
    await fs.access(value)
    return true
  } catch {
    return false
  }
}

const readMaterialSymbolNames = async () => {
  const source = await fs.readFile(symbolTypesPath, 'utf8')
  const names = new Set()
  for (const match of source.matchAll(/"([a-z0-9][a-z0-9_]*)"/g)) {
    names.add(match[1])
  }
  return names
}

const walkSourceFiles = async root => {
  const files = []
  if (!await pathExists(root)) return files

  const entries = await fs.readdir(root, { withFileTypes: true })
  for (const entry of entries) {
    if (ignoredDirectories.has(entry.name)) continue

    const fullPath = path.join(root, entry.name)
    if (entry.isDirectory()) {
      files.push(...await walkSourceFiles(fullPath))
      continue
    }

    if (sourceExtensions.has(path.extname(entry.name))) {
      files.push(fullPath)
    }
  }

  return files
}

const collectStringLiterals = source => {
  const values = []
  for (const match of source.matchAll(/(['"`])((?:\\.|(?!\1)[^\\])*?)\1/g)) {
    values.push(match[2])
  }
  return values
}

const collectMaterialFileTokens = source => {
  if (!source.includes('material-symbols-rounded')) return []
  return Array.from(source.matchAll(/\b[a-z0-9][a-z0-9_]*\b/g), match => match[0])
}

const collectUsedSymbols = async availableSymbols => {
  const symbols = new Set(explicitSymbols)

  for (const root of scanRoots) {
    for (const file of await walkSourceFiles(root)) {
      const source = await fs.readFile(file, 'utf8')
      const candidates = [
        ...collectStringLiterals(source),
        ...collectMaterialFileTokens(source)
      ]

      for (const candidate of candidates) {
        if (isMaterialSymbol(candidate) && availableSymbols.has(candidate)) {
          symbols.add(candidate)
        }
      }
    }
  }

  return Array.from(symbols).sort((a, b) => a.localeCompare(b))
}

const main = async () => {
  const availableSymbols = await readMaterialSymbolNames()
  const symbols = await collectUsedSymbols(availableSymbols)
  const sourceFont = await fs.readFile(sourceFontPath)
  const iconText = symbols.join(' ')
  const subset = await subsetFont(sourceFont, iconText, {
    targetFormat: 'woff2',
    variationAxes: {
      FILL: 0,
      GRAD: 0,
      opsz: 24,
      wght: 300
    }
  })

  await fs.mkdir(path.dirname(outputFontPath), { recursive: true })
  await fs.writeFile(outputFontPath, subset)
  await fs.writeFile(
    outputListPath,
    `${
      [
        '# Generated by pnpm -C apps/client icons:subset',
        `# Symbols: ${symbols.length}`,
        '',
        ...symbols
      ].join('\n')
    }\n`
  )

  console.log([
    `Material Symbols Rounded subset: ${symbols.length} symbols`,
    `${formatBytes(sourceFont.length)} -> ${formatBytes(subset.length)}`,
    path.relative(clientRoot, outputFontPath)
  ].join('\n'))
}

await main()
